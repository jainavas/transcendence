/**
 * Sistema de Internacionalizaci√≥n (i18n) para Transcendence
 * Soporte para m√∫ltiples idiomas con detecci√≥n autom√°tica
 */

class I18nManager {
    constructor() {
        this.currentLanguage = 'es'; // Idioma por defecto
        this.fallbackLanguage = 'es';
        this.translations = {};
        this.supportedLanguages = ['es', 'en', 'fr', 'de', 'pt'];
        this.languageNames = {
            'es': 'Espa√±ol',
            'en': 'English',
            'fr': 'Fran√ßais', 
            'de': 'Deutsch',
            'pt': 'Portugu√™s'
        };
        this.isLoaded = false;
        this.loadPromise = null;
        this.observers = []; // Para notificar cambios de idioma
    }

    /**
     * Inicializa el sistema de i18n
     */
    async init() {
        if (this.loadPromise) {
            return this.loadPromise;
        }

        this.loadPromise = this._performInit();
        return this.loadPromise;
    }

    async _performInit() {
        try {
            // Detectar idioma del navegador o cargar desde localStorage
            this.currentLanguage = this.detectLanguage();
            
            // Cargar traducciones
            await this.loadTranslations(this.currentLanguage);
            
            // Aplicar traducciones a la p√°gina
            this.applyTranslations();
            
            this.isLoaded = true;
            console.log(`üåç i18n inicializado en idioma: ${this.currentLanguage}`);
            
            // Notificar a los observadores
            this.notifyObservers();
            
        } catch (error) {
            console.error('‚ùå Error inicializando i18n:', error);
            // Fallback al idioma por defecto
            if (this.currentLanguage !== this.fallbackLanguage) {
                this.currentLanguage = this.fallbackLanguage;
                await this.loadTranslations(this.currentLanguage);
                this.applyTranslations();
            }
        }
    }

    /**
     * Detecta el idioma preferido del usuario
     */
    detectLanguage() {
        // 1. Verificar localStorage
        const saved = localStorage.getItem('transcendence_language');
        if (saved && this.supportedLanguages.includes(saved)) {
            return saved;
        }

        // 2. Detectar del navegador
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.substring(0, 2).toLowerCase();
        
        if (this.supportedLanguages.includes(langCode)) {
            return langCode;
        }

        // 3. Fallback al idioma por defecto
        return this.fallbackLanguage;
    }

    /**
     * Carga las traducciones para un idioma espec√≠fico
     */
    async loadTranslations(lang) {
        try {
            console.log(`üåç Intentando cargar traducciones para ${lang}...`);
            const response = await fetch(`locales/${lang}.json`);
            if (!response.ok) {
                throw new Error(`Error cargando idioma ${lang}: ${response.status}`);
            }
            
            this.translations[lang] = await response.json();
            console.log(`‚úÖ Traducciones cargadas para ${lang}:`, Object.keys(this.translations[lang]));
            
        } catch (error) {
            console.error(`‚ùå Error cargando traducciones para ${lang}:`, error);
            
            // Si falla el idioma actual, usar fallback
            if (lang !== this.fallbackLanguage) {
                console.log(`üîÑ Cargando idioma fallback: ${this.fallbackLanguage}`);
                await this.loadTranslations(this.fallbackLanguage);
            } else {
                // Si hasta el fallback falla, crear traducciones b√°sicas
                console.warn('‚ö†Ô∏è Creando traducciones b√°sicas de emergencia...');
                this.translations[lang] = {
                    "common": { "loading": "Cargando...", "error": "Error" },
                    "dashboard": { "welcome": "Bienvenido", "title": "Dashboard" },
                    "game": { "score": "Puntuaci√≥n", "game_over": "Juego terminado" }
                };
            }
        }
    }

    /**
     * Obtiene una traducci√≥n por clave
     */
    t(key, params = {}) {
        const lang = this.currentLanguage;
        let translation = this.getNestedValue(this.translations[lang], key);
        
        // Fallback al idioma por defecto si no existe la traducci√≥n
        if (!translation && lang !== this.fallbackLanguage) {
            translation = this.getNestedValue(this.translations[this.fallbackLanguage], key);
        }
        
        // Si a√∫n no existe, devolver la clave
        if (!translation) {
            console.warn(`üîç Traducci√≥n no encontrada: ${key}`);
            return key;
        }

        // Reemplazar par√°metros en la traducci√≥n
        return this.interpolate(translation, params);
    }

    /**
     * Obtiene valor anidado de un objeto usando notaci√≥n de puntos
     */
    getNestedValue(obj, path) {
        return path.split('.').reduce((current, key) => {
            return current && current[key] !== undefined ? current[key] : null;
        }, obj);
    }

    /**
     * Interpola par√°metros en una cadena de traducci√≥n
     */
    interpolate(template, params) {
        return template.replace(/\{\{(\w+)\}\}/g, (match, key) => {
            return params[key] !== undefined ? params[key] : match;
        });
    }

    /**
     * Cambia el idioma actual
     */
    async changeLanguage(newLang) {
        if (!this.supportedLanguages.includes(newLang)) {
            console.error(`‚ùå Idioma no soportado: ${newLang}`);
            return false;
        }

        if (newLang === this.currentLanguage) {
            return true; // Ya est√° en ese idioma
        }

        try {
            // Cargar traducciones del nuevo idioma si no est√°n cargadas
            if (!this.translations[newLang]) {
                await this.loadTranslations(newLang);
            }

            this.currentLanguage = newLang;
            localStorage.setItem('transcendence_language', newLang);
            
            this.applyTranslations();
            this.notifyObservers();
            
            console.log(`üåç Idioma cambiado a: ${newLang}`);
            return true;
            
        } catch (error) {
            console.error(`‚ùå Error cambiando idioma a ${newLang}:`, error);
            return false;
        }
    }

    /**
     * Aplica las traducciones a todos los elementos con data-i18n
     */
    applyTranslations() {
        // Traducir elementos con data-i18n
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            
            // Decidir si es texto o atributo
            if (element.hasAttribute('data-i18n-attr')) {
                const attr = element.getAttribute('data-i18n-attr');
                element.setAttribute(attr, translation);
            } else {
                element.textContent = translation;
            }
        });

        // Traducir placeholders
        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            element.placeholder = this.t(key);
        });

        // Traducir t√≠tulos
        const titles = document.querySelectorAll('[data-i18n-title]');
        titles.forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            element.title = this.t(key);
        });
        
        // Actualizar textos din√°micos del juego
        this.updateGameTexts();
    }

    /**
     * Registra un observador para cambios de idioma
     */
    onLanguageChange(callback) {
        this.observers.push(callback);
    }

    /**
     * Notifica a todos los observadores sobre cambios de idioma
     */
    notifyObservers() {
        this.observers.forEach(callback => {
            try {
                callback(this.currentLanguage);
            } catch (error) {
                console.error('‚ùå Error en observador de idioma:', error);
            }
        });
    }

    /**
     * Obtiene la lista de idiomas soportados
     */
    getSupportedLanguages() {
        return this.supportedLanguages.map(code => ({
            code,
            name: this.languageNames[code]
        }));
    }

    /**
     * Obtiene el idioma actual
     */
    getCurrentLanguage() {
        return this.currentLanguage;
    }

    /**
     * Verifica si el sistema est√° listo
     */
    isReady() {
        return this.isLoaded;
    }

    /**
     * Actualiza textos din√°micos en los juegos de Pong
     */
    updateGameTexts() {
        // Actualizar el mensaje de inicio si existe
        if (window.mensajeInicio) {
            window.mensajeInicio.text = this.t('game.choose_character_and_environment');
        }
        
        // Actualizar marcador usando la funci√≥n especializada si existe
        if (window.updateScoreDisplay) {
            window.updateScoreDisplay();
        } else {
            // Fallback: actualizar manualmente
            const scoreElement = document.getElementById('score');
            if (scoreElement && window.scoreP1 !== undefined) {
                // Detectar modo de juego basado en si hay 4 jugadores activos
                const is4PlayerMode = window.scoreP3 !== undefined && window.scoreP4 !== undefined;
                
                if (is4PlayerMode) {
                    // Modo 4 jugadores
                    const blueText = this.t('game.blue');
                    const redText = this.t('game.red');
                    const greenText = this.t('game.green');
                    const purpleText = this.t('game.purple');
                    scoreElement.textContent = `${blueText}:${window.scoreP1} - ${redText}:${window.scoreP2} - ${greenText}:${window.scoreP3} - ${purpleText}:${window.scoreP4}`;
                } else {
                    // Modo 2 jugadores
                    scoreElement.textContent = `${window.scoreP1} - ${window.scoreP2}`;
                }
            }
        }
        
        console.log('‚úÖ Textos del juego actualizados al idioma:', this.currentLanguage);
    }
}

// Crear instancia global
window.i18n = new I18nManager();

// Funciones de conveniencia globales
window.t = (key, params) => window.i18n.t(key, params);
window.changeLanguage = (lang) => window.i18n.changeLanguage(lang);

// Auto-inicializar cuando el DOM est√© listo
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        console.log('üåç DOM cargado, inicializando i18n...');
        window.i18n.init();
    });
} else {
    console.log('üåç DOM ya cargado, inicializando i18n inmediatamente...');
    window.i18n.init();
}

// Debug: verificar que todo est√© disponible
console.log('üîß i18n.js cargado, funciones disponibles:', {
    i18n: !!window.i18n,
    t: !!window.t,
    changeLanguage: !!window.changeLanguage
});
